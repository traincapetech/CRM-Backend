const mongoose = require('mongoose');

const testUserGroupSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
    unique: true
  },
  description: {
    type: String,
    trim: true,
    default: ''
  },
  members: [{
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User'
  }],
  createdBy: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  isActive: {
    type: Boolean,
    default: true
  }
}, {
  timestamps: true
});

testUserGroupSchema.index({ name: 1 });
testUserGroupSchema.index({ createdBy: 1 });

module.exports = mongoose.model('TestUserGroup', testUserGroupSchema);
